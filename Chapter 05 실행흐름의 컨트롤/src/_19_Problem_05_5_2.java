public class _19_Problem_05_5_2 {

	public static void main(String[] args) {
		System.out.println("문제 5-5 [for문의 활용]");
		System.out.println("문제 2)");
		
		int dan = 5;
		
		for (int i = 1; i < 10; i++)
			System.out.println("5 X " + i + " = " + dan * i);
	}

}

